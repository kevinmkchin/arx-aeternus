package tools;

public class AMVector {

    public float x;
    public float y;
    public float z;

    public AMVector()
    {
        x = 0;
        y = 0;
        z = 0;
    }

    public AMVector(float x, float y, float z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }

}
